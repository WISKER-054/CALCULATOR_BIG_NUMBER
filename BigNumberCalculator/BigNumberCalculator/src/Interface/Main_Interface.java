    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter txt = new FileNameExtensionFilter("Text files (.txt)", "txt");
        fileChooser.setFileFilter(txt);
        fileChooser.setMultiSelectionEnabled(false);

        int x = fileChooser.showSaveDialog(this);
        if (x == JFileChooser.APPROVE_OPTION) {
        File file = fileChooser.getSelectedFile();

        if(asm == '+'){
            try (BufferedWriter bw = new BufferedWriter(new FileWriter(file + ".txt"))) {

            String num1Str = textNumberOfA.getText();
            String num2Str = textNumberOfB.getText();
            String num3Str = resultOfCalculation.getText();

            bw.write("Number A = " + num1Str);
            bw.newLine();
            bw.write("Number B = " + num2Str);
            bw.newLine();
            bw.write("Results A + B = " + num3Str);
        } catch (IOException e) {
        }
        }
        if(asm == '-'){
            try (BufferedWriter bw = new BufferedWriter(new FileWriter(file + ".txt"))) {

            String num1Str = textNumberOfA.getText();
            String num2Str = textNumberOfB.getText();
            String num3Str = resultOfCalculation.getText();

            bw.write("Number A = " + num1Str);
            bw.newLine();
            bw.write("Number B = " + num2Str);
            bw.newLine();
            bw.write("Results A - B = " + num3Str);
        } catch (IOException e) {
        }
        }
        if(asm == '*'){
            try (BufferedWriter bw = new BufferedWriter(new FileWriter(file + ".txt"))) {

            String num1Str = textNumberOfA.getText();
            String num2Str = textNumberOfB.getText();
            String num3Str = resultOfCalculation.getText();

            bw.write("Number A = " + num1Str);
            bw.newLine();
            bw.write("Number B = " + num2Str);
            bw.newLine();
            bw.write("Results A x B = " + num3Str);
        } catch (IOException e) {
        }
        }
        if(asm == '/'){
            try (BufferedWriter bw = new BufferedWriter(new FileWriter(file + ".txt"))) {

            String num1Str = textNumberOfA.getText();
            String num2Str = textNumberOfB.getText();
            String num3Str = resultOfCalculation.getText();
            String num4Str = numberRemainder.getText();

            bw.write("Number A = " + num1Str);
            bw.newLine();
            bw.write("Number B = " + num2Str);
            bw.newLine();
            bw.write("Results A / B = " + num3Str);
            bw.newLine();
            bw.write("numberRemainder = " + num4Str);
        } catch (IOException e) {
        }
        }
        }
    }//GEN-LAST:event_saveActionPerformed

