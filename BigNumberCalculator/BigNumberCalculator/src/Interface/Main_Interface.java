    private void randomNumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_randomNumberActionPerformed

        Random random = new Random();
        int n = random.nextInt(20);
        // Generate non-empty number for Number A
        String a = "";
        int q;
        do {
            q = random.nextInt(10);
            a += String.valueOf(q);
        } while (q == 0);
        for (int i = 1; i < n; i++) {
            q = random.nextInt(10);
            a += String.valueOf(q);
        }
        textNumberOfA.setText(a);

        n = random.nextInt(20);
        // Generate non-empty number for Number B
        a = "";
        do {
            q = random.nextInt(10);
            a += String.valueOf(q);
        } while (q == 0);
        for (int i = 1; i < n; i++) {
            q = random.nextInt(10);
            a += String.valueOf(q);
        }
        textNumberOfB.setText(a);
    }//GEN-LAST:event_randomNumberActionPerformed

    private void OpenFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenFileActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter txt = new FileNameExtensionFilter("Text files (.txt)", "txt");
        fileChooser.setFileFilter(txt);
        fileChooser.setMultiSelectionEnabled(false);
        File file = null;
        String num1 = "";
        String num2 = "";

        int x = fileChooser.showOpenDialog(this);
        if (x == JFileChooser.APPROVE_OPTION) {
            file = fileChooser.getSelectedFile();
            try (BufferedReader br = new BufferedReader(new FileReader(file))) {
                // Read the first line and extract only numbers
                String line = br.readLine();
                num1 = line.replaceAll("[^0-9]", "");

                // Read the second line and extract only numbers
                line = br.readLine();
                num2 = line.replaceAll("[^0-9]", "");
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
        // Display the extracted numbers
        textNumberOfA.setText(num1);
        textNumberOfB.setText(num2);
    }//GEN-LAST:event_OpenFileActionPerformed

